This is the first alpha release of Spamikaze. The software works and
is in production on our own systems, but there are some rough edges
left, as well as many TODO items.

<p>Get
<a href="ftp://ftp.nl.linux.org/pub/spamikaze/spamikaze-0.1.tar.gz">spamikaze-0.1.tar.gz</a> via ftp.

<h2>CVS snapshots</h2>

Spamikaze development can be followed by getting daily CVS snapshots
from Spamikaze's <a href="ftp://ftp.nl.linux.org/pub/spamikaze/">ftp
directory</a>.  Note that there often is experimental code in CVS, so
only use these snapshots if you know what you're doing.

